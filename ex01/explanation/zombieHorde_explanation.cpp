#include "Zombie.hpp" // Zombieクラスの定義を含むヘッダーファイルをインクルードします。

// zombieHorde関数
// 指定された数Nのゾンビを生成し、それぞれに指定された名前を付けます。
// 戻り値は、生成されたZombieオブジェクトの配列へのポインタです。
Zombie* zombieHorde( int N, std::string name )
{
    Zombie *zombieHorde; // Zombieオブジェクトのポインタを宣言します。ゾンビの群れ（配列）を指すために使われます。
    int i;               // ループカウンタとして使用する整数変数です。

    // new演算子を使って、N個のZombieオブジェクトのためのメモリをヒープに動的に割り当てます。
    // これにより、Zombieオブジェクトの配列が作成され、その先頭へのポインタがzombieHordeに代入されます。
    zombieHorde = new Zombie[N];

    i = 0; // ループカウンタiを0で初期化します。
    // whileループを使って、生成された各ゾンビオブジェクトを初期化します。
    while(i < N) // iがN未満の間、ループを続けます。
    {
        // 配列のi番目の要素に、新しいZombieオブジェクト（指定された名前で初期化されたもの）を代入します。
        // ここでは、デフォルトコンストラクタで作成されたZombieオブジェクトに、引数付きコンストラクタで作成された
        // 一時的なZombieオブジェクトが代入（コピーコンストラクタまたは代入演算子が呼び出される）されます。
        zombieHorde[i] = Zombie(name);
        i++; // ループカウンタiをインクリメントします。
    }
    return(zombieHorde); // 生成され、初期化されたゾンビの群れ（配列）へのポインタを返します。
}

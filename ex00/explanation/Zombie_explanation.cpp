#include "Zombie.hpp" // Zombieクラスの定義（Zombie.hpp）を読み込みます。
                      // これでZombieクラスの設計図が使えるようになります。

// ----------------------------------------------------------------------
// Zombieクラスのコンストラクタ
// ----------------------------------------------------------------------
// Zombie::Zombie(std::string name)
// これは「ゾンビが生まれたとき」に自動的に呼び出される特別な関数です。
// 'name' という引数を受け取って、そのゾンビの名前を設定します。
//
// 例えば、Zombie("太郎") と書くと、この関数が動きます。
Zombie::Zombie(std::string name)
{
    // 'this->name' は、今まさに作られているゾンビ自身の名前（プライベート変数）を指します。
    // 引数として受け取った 'name' を、このゾンビの名前として保存します。
    this->name = name; 

    // ゾンビが作られたことを示すメッセージを画面に出力します。
    // 例：「太郎 : created.」のように表示されます。
    std::cout << this->name << " : created." << std::endl;
}

// ----------------------------------------------------------------------
// Zombieクラスのデストラクタ
// ----------------------------------------------------------------------
// Zombie::~Zombie()
// これは「ゾンビが消滅するとき」に自動的に呼び出される特別な関数です。
// メモリからゾンビが取り除かれる直前に、最後の処理を行います。
//
// ヒープで作られたゾンビは 'delete' された時、
// スタックで作られたゾンビは、その関数が終わった時に自動的に呼び出されます。
Zombie::~Zombie()
{
    // ゾンビが消滅したことを示すメッセージを画面に出力します。
    // 例：「太郎 : destroyed.」のように表示されます。
    // このメッセージは、ゾンビがいつメモリから片付けられたかを確認するのに役立ちます。
    std::cout << this->name << " : destroyed." << std::endl;
}

// ----------------------------------------------------------------------
// announceメンバー関数
// ----------------------------------------------------------------------
// void Zombie::announce(void)
// これはゾンビが「自己紹介する」ための関数です。
// ゾンビオブジェクトに対して、例えば 'myZombie.announce();' のように呼び出します。
void Zombie::announce(void)
{
    // ゾンビの名前と、お決まりのセリフ「BraiiiiiiinnnzzzZ...」を画面に出力します。
    // 例：「太郎 : BraiiiiiiinnnzzzZ...」のように表示されます。
    std::cout << this->name << " : BraiiiiiiinnnzzzZ..." << std::endl;
}
